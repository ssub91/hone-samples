CREATE TABLE PERSON(
    NO NUMBER(10) PRIMARY KEY NOT NULL,
    FIRST_NAME VARCHAR(20),
    LAST_NAME VARCHAR(20)
);

CREATE TABLE PEOPLE(
    NO NUMBER(10) PRIMARY KEY NOT NULL,
    FULL_NAME VARCHAR(40)
);


CREATE SEQUENCE PERSON_SEQ
START WITH 1 
INCREMENT BY 1 
MAXVALUE 9999999999;

CREATE SEQUENCE PEOPLE_SEQ
START WITH 1 
INCREMENT BY 1 
MAXVALUE 9999999999;